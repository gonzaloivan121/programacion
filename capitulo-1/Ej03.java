public class Ej03 {
  public static void main(String[] args) {
    System.out.println("English         Español");
    System.out.println("------------------------");
    System.out.printf("%-10s      %-10s\n", "Hello", "Hola");
    System.out.printf("%-10s      %-10s\n", "Goodbye", "Adiós");
    System.out.printf("%-10s      %-10s\n", "Sorry", "Perdón");
    System.out.printf("%-10s      %-10s\n", "Tree", "Árbol");
    System.out.printf("%-10s      %-10s\n", "Orange", "Naranja");
    System.out.printf("%-10s      %-10s\n", "Apple", "Manzana");
    System.out.printf("%-10s      %-10s\n", "Pineapple", "Piña");
    System.out.printf("%-10s      %-10s\n", "Eight", "Ocho");
    System.out.printf("%-10s      %-10s\n", "Casa", "House");
    System.out.printf("%-10s      %-10s\n", "Window", "Ventana");

  }
}


// %d = nº entero
// %f = nº con decimales
// %s = cadena de caracteres
